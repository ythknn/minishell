static void clear_terminal(void)
{
	printf("\033[2J\033[H");
	fflush(stdout);
}

static int restore_blocking(int fd)
{
	int flags = fcntl(fd, F_GETFL, 0);
	if (flags == -1)
		return (-1);
	return (fcntl(fd, F_SETFL, flags & ~O_NONBLOCK));
}

static void enable_ctrl_c(void)
{
	struct termios term;

	tcgetattr(STDIN_FILENO, &g_original_term);
	tcgetattr(STDIN_FILENO, &term);
	term.c_lflag |= ISIG;
	tcsetattr(STDIN_FILENO, TCSANOW, &term);
}

static void restore_terminal(void)
{
	tcsetattr(STDIN_FILENO, TCSANOW, &g_original_term);
}

static void flash_screen(void)
{
	int i;
	for (i = 0; i < 12; i++)
	{
		if (i % 2 == 0)
			printf("\033[47m\033[2J\033[H");
		else
			printf("\033[40m\033[2J\033[H");
		fflush(stdout);
		usleep(100000);
	}
	printf("\033[0m\033[2J\033[H");
	fflush(stdout);
}

